function [output]=d_fft16(input);
f1 = zeros(16,1);f2 = zeros(16,1);f3 = zeros(16,1);f4 = zeros(16,1);output  = zeros(16,1);
for s = 0:1:7
    W=exp(-2*pi*j*s*1/16);
    f1(s+1) = input(s+1)  + input(s+9);
    f1(s+9) = (input(s+1) - input(s+9))*W;
end;
for s = 0:1:3
   	W=exp(-2*pi*j*s*1/8);
   	f2(s+1)  = f1(s+1)  + f1(s+5);
   	f2(s+5)  = (f1(s+1) - f1(s+5))*W;
   	f2(s+9)  = (f1(s+9) + f1(s+13));
   	f2(s+13)  = (f1(s+9) - f1(s+13))*W;
end;
for s = 0:1:1
   	W=exp(-2*pi*j*s*1/4);
   	f3(s+1)  = f2(s+1)  + f2(s+3);
   	f3(s+3)  = (f2(s+1) - f2(s+3))*W;
   	f3(s+5)  = (f2(s+5) + f2(s+7));
   	f3(s+7)  = (f2(s+5) - f2(s+7))*W;
   	f3(s+9)  = (f2(s+9) + f2(s+11));
   	f3(s+11)  = (f2(s+9) - f2(s+11))*W;  
   	f3(s+13)  = (f2(s+13) + f2(s+15));
   	f3(s+15)  = (f2(s+13) - f2(s+15))*W; 	
end;
    W=exp(-2*pi*j*0*1/2);
    f4(1) = f3(1)  + f3(2);
    f4(2) = (f3(1)  - f3(2))*W;
    f4(3) = f3(3)  + f3(4);
    f4(4) = (f3(3)  - f3(4))*W;
    f4(5) = f3(5)  + f3(6);
    f4(6) = (f3(5)  - f3(6))*W;
    f4(7) = f3(7)  + f3(8);
    f4(8) = (f3(7)  - f3(8))*W;
    f4(9) = f3(9)  + f3(10);
    f4(10) = (f3(9)  - f3(10))*W;
    f4(11) = f3(11)  + f3(12);
    f4(12) = (f3(11)  - f3(12))*W;
    f4(13) = f3(13)  + f3(14);
    f4(14) = (f3(13)  - f3(14))*W;
    f4(15) = f3(15)  + f3(16);
    f4(16) = (f3(15)  - f3(16))*W;
         
output(1) = f4(1);
output(2) = f4(9);
output(3) = f4(5);
output(4) = f4(13);
output(5) = f4(3);
output(6) = f4(11);
output(7) = f4(7);
output(8) = f4(15);
output(9) = f4(2);
output(10) = f4(10);
output(11) = f4(6);
output(12) = f4(14);
output(13) = f4(4);
output(14) = f4(12);
output(15) = f4(8);
output(16) = f4(16);